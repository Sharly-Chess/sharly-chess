"""FFE Molter pairing tables — data only.

Encoded from the FFE DNA "Tableaux Molter" reference (October 2025), covering
3-13 teams × {4, 6, 8, 10, 12} players-per-team. Each table is keyed
``(team_count, players_per_team)``.

Each round is a tuple of pairings indexed by board number (board 1 →
index 0). A pairing ``P('A', 1, 'C', 1)`` means: team A's 1st player has
white, team C's 1st player has black. Letters identify teams in the order
they were sorted (A = first team by pairing_number, B = second, ...). The
FFE source skips letter "I" to avoid visual collision with "1"; the parser
remaps J→I, K→J, etc., so this file uses sequential A-N letters.

The ``autonomous_round`` is the FFE "Ronde autonome" — used as a stand-alone
1-round event, or appended as the terminal round when the tournament has an
odd number of rounds.

Several FFE tables cover multiple player-counts (e.g. Tableau 1 covers
4 / 8 / 12 players for 3 teams). Such tables are stored once at their
maximum size and exposed under the smaller keys via ``_truncate``.

PDF source contains a handful of printing/numbering typos that the data
in this file silently corrects:
  - Tableau 2 round 2 boards 4-5 and autonomous board 5 (duplicate B4/C4).
  - Tableau 6 round 2 board 16 (printed "A8 – A8", teammate pairing).
  - Tableau 28 numbered twice (the second is Tableau 32 for 11 teams).
  - Tableau 36 header says "20 équipes" but the data is for 12 teams.

Auto-generated portions of this file were produced from the FFE DNA PDF
via pdftotext -raw + a parser; the registry mapping below is curated.
"""

from data.pairings.fixed_table import FixedPairingTable, TablePairing as P


def _truncate(
    base: FixedPairingTable, players_per_team: int, board_count: int
) -> FixedPairingTable:
    """Return ``base`` restricted to its first ``board_count`` boards per
    round, labelled with the given ``players_per_team``. Valid only when the
    leading ``board_count`` boards reference only players 1..players_per_team
    on every team (true for FFE Molter tables by construction)."""
    return FixedPairingTable(
        team_count=base.team_count,
        players_per_team=players_per_team,
        rounds=tuple(tuple(r[:board_count]) for r in base.rounds),
        autonomous_round=(
            tuple(base.autonomous_round[:board_count])
            if base.autonomous_round is not None
            else None
        ),
    )


